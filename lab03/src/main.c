int main()
{
	int sum = 0;		//variable for operation's result 
	int min_num = 100;		//first minimal number
	int max_num = 12;		// second maximal number
	int diff = max_num - min_num + 1;	//number of numbers
	sum = ((max_num + min_num) * diff) / 2; //sum of n-numbers in arifmetic progression
	return 0;
}
